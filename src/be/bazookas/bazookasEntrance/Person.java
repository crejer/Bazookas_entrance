package be.bazookas.bazookasEntrance;



public class Person {
	private String _naam;
	private String _company;
	
	

	public Person() {
		
	}

	public String get_naam() {
		return _naam;
	}
	public void set_naam(String _naam) {
		this._naam = _naam;
	}
	public String get_company() {
		return _company;
	}
	public void set_company(String _company) {
		this._company = _company;
	}
	
}
